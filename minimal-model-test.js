const tf = require('@tensorflow/tfjs-node');
const fs = require('fs');
const path = require('path');

async function testMinimalModel() {
    try {
        console.log('TensorFlow.js version:', tf.version.tfjs);
        
        // Load the model.json file directly
        const modelPath = path.join(__dirname, 'public', 'calendar_event_classification_model', 'model.json');
        const modelJson = JSON.parse(fs.readFileSync(modelPath, 'utf8'));
        
        console.log('Model format:', modelJson.format);
        console.log('Generated by:', modelJson.generatedBy);
        console.log('Converted by:', modelJson.convertedBy);
        console.log('Keras version:', modelJson.modelTopology.keras_version);
        
        // Check all InputLayers
        const layers = modelJson.modelTopology.model_config.config.layers;
        const inputLayers = layers.filter(layer => layer.class_name === 'InputLayer');
        
        console.log('\nFound InputLayers:');
        inputLayers.forEach((layer, i) => {
            console.log(`InputLayer ${i + 1}:`, layer.name);
            console.log('  Config keys:', Object.keys(layer.config));
            console.log('  Has batchInputShape:', 'batchInputShape' in layer.config);
            console.log('  Has inputShape:', 'inputShape' in layer.config);
            console.log('  Has batch_shape:', 'batch_shape' in layer.config);
            if (layer.config.batchInputShape) {
                console.log('  batchInputShape:', layer.config.batchInputShape);
            }
            if (layer.config.inputShape) {
                console.log('  inputShape:', layer.config.inputShape);
            }
        });
        
        // Try to create a simple model with the same InputLayer config
        console.log('\nTrying to create a simple model with same InputLayer config...');
        
        const input = tf.input({
            shape: [15],
            name: 'test_input',
            dtype: 'int32'
        });
        
        const simpleModel = tf.model({
            inputs: input,
            outputs: input
        });
        
        console.log('Simple model created successfully!');
        console.log('Simple model summary:');
        simpleModel.summary();
        
        // Now try to load the actual model
        console.log('\nNow trying to load the actual model...');
        const modelUrl = `file://${path.join(__dirname, 'public', 'calendar_event_classification_model', 'model.json')}`;
        const model = await tf.loadLayersModel(modelUrl);
        
        console.log('Model loaded successfully!');
        console.log('Model summary:');
        model.summary();
        
    } catch (error) {
        console.error('Error:', error.message);
        console.error('Stack:', error.stack);
    }
}

testMinimalModel();
