// Alternative test using file system and proper TensorFlow.js Node.js setup
const tf = require('@tensorflow/tfjs-node');
const fs = require('fs');
const path = require('path');

console.log('Starting TensorFlow.js Node.js test...');
console.log('TensorFlow.js version:', tf.version.tfjs);

async function testModelLoading() {
  try {
    console.log('Testing model loading with Node.js backend...');
    
    // Use Node.js file loading approach
    const modelPath = path.join(__dirname, 'public', 'calendar_event_classification_model', 'model.json');
    console.log('Model path:', modelPath);
    
    // Check if files exist
    if (!fs.existsSync(modelPath)) {
      throw new Error('Model file does not exist at: ' + modelPath);
    }
    
    const weightsPath1 = path.join(__dirname, 'public', 'calendar_event_classification_model', 'group1-shard1of2.bin');
    const weightsPath2 = path.join(__dirname, 'public', 'calendar_event_classification_model', 'group1-shard2of2.bin');
    
    if (!fs.existsSync(weightsPath1) || !fs.existsSync(weightsPath2)) {
      throw new Error('Weight files do not exist');
    }
    
    console.log('All model files exist, attempting to load...');
    
    // Try loading with file:// protocol
    const fileUrl = 'file://' + modelPath;
    console.log('Loading from URL:', fileUrl);
    
    // Try with loadLayersModel and proper error handling
    try {
      const model = await tf.loadLayersModel(fileUrl);
      console.log('Model loaded successfully!');
      
      console.log('Model inputs:', model.inputs.map(input => ({ 
        name: input.name, 
        shape: input.shape,
        dtype: input.dtype
      })));
      console.log('Model outputs:', model.outputs.map(output => ({ 
        name: output.name, 
        shape: output.shape,
        dtype: output.dtype
      })));
      
      // Test prediction
      console.log('Testing model prediction...');
      
      const testNameTensor = tf.zeros([1, 15], 'int32');
      const testStartTensor = tf.zeros([1, 4], 'float32');
      const testEndTensor = tf.zeros([1, 4], 'float32');
      
      // Test with array format (most compatible)
      console.log('Testing with array input format...');
      const arrayPrediction = model.predict([testNameTensor, testStartTensor, testEndTensor]);
      console.log('Array prediction successful!');
      console.log('Category output shape:', arrayPrediction[0].shape);
      console.log('Subcategory output shape:', arrayPrediction[1].shape);
      
      // Clean up
      testNameTensor.dispose();
      testStartTensor.dispose();
      testEndTensor.dispose();
      arrayPrediction[0].dispose();
      arrayPrediction[1].dispose();
      
      console.log('Node.js test completed successfully!');
      
    } catch (loadError) {
      console.error('Model loading failed:', loadError.message);
      
      // Try to read and analyze the model.json directly
      console.log('Analyzing model.json structure...');
      const modelJson = JSON.parse(fs.readFileSync(modelPath, 'utf8'));
      console.log('Model format:', modelJson.format);
      console.log('Generated by:', modelJson.generatedBy);
      console.log('Converted by:', modelJson.convertedBy);
      
      // Check input layers
      const inputLayers = modelJson.modelTopology.model_config.config.layers.filter(layer => 
        layer.class_name === 'InputLayer'
      );
      console.log('Input layers found:', inputLayers.length);
      inputLayers.forEach((layer, i) => {
        console.log(`  Input ${i + 1}:`, {
          name: layer.name,
          batch_shape: layer.config.batch_shape,
          dtype: layer.config.dtype
        });
      });
      
      throw loadError;
    }
    
  } catch (error) {
    console.error('Test failed:', error.message);
    console.error('Error stack:', error.stack);
    
    // Additional debugging
    console.log('\nDebugging information:');
    console.log('Current working directory:', process.cwd());
    console.log('Node.js version:', process.version);
    console.log('TensorFlow.js backend:', tf.getBackend());
  }
}

// Run the test
testModelLoading();
