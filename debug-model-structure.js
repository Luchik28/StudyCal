const fs = require('fs');
const path = require('path');

function debugModelStructure() {
    console.log('=== DEBUGGING MODEL STRUCTURE ===');
    
    const modelPath = path.join(__dirname, 'public', 'calendar_event_classification_model', 'model.json');
    
    try {
        console.log('1. Reading model file...');
        const modelJson = JSON.parse(fs.readFileSync(modelPath, 'utf8'));
        
        console.log('2. Model basic info:');
        console.log('   Format:', modelJson.format);
        console.log('   Generated by:', modelJson.generatedBy);
        console.log('   Converted by:', modelJson.convertedBy);
        
        console.log('3. Checking layers...');
        const layers = modelJson.modelTopology.model_config.config.layers;
        console.log('   Total layers:', layers.length);
        
        layers.forEach((layer, i) => {
            console.log(`\n   Layer ${i}: ${layer.class_name} (${layer.name})`);
            
            if (layer.inbound_nodes) {
                console.log(`     Inbound nodes type: ${Array.isArray(layer.inbound_nodes) ? 'Array' : 'Object'}`);
                if (Array.isArray(layer.inbound_nodes)) {
                    console.log(`     Inbound nodes length: ${layer.inbound_nodes.length}`);
                    layer.inbound_nodes.forEach((node, j) => {
                        console.log(`       Node ${j}: ${typeof node}`);
                        if (Array.isArray(node)) {
                            console.log(`         Array length: ${node.length}`);
                            console.log(`         First element: ${JSON.stringify(node[0])}`);
                        } else if (typeof node === 'object') {
                            console.log(`         Object keys: ${Object.keys(node)}`);
                        }
                    });
                }
            }
        });
        
        console.log('\n4. Checking input/output layers...');
        const config = modelJson.modelTopology.model_config.config;
        console.log('   Input layers:', JSON.stringify(config.input_layers));
        console.log('   Output layers:', JSON.stringify(config.output_layers));
        
        console.log('\n5. File size check...');
        const stats = fs.statSync(modelPath);
        console.log('   File size:', stats.size, 'bytes');
        
    } catch (error) {
        console.error('Error reading model:', error.message);
    }
}

debugModelStructure();
