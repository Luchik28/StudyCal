const tf = require('@tensorflow/tfjs-node');
const fs = require('fs');
const path = require('path');

async function debugModelLoading() {
    console.log('=== DEBUGGING MODEL LOADING ===');
    
    const modelPath = path.join(__dirname, 'public', 'calendar_event_classification_model', 'model.json');
    console.log('Model path:', modelPath);
    
    try {
        // Step 1: Check if file exists and is readable
        console.log('\n1. Checking file accessibility...');
        const stats = fs.statSync(modelPath);
        console.log('   File size:', stats.size, 'bytes');
        console.log('   File accessible: OK');
        
        // Step 2: Try to parse JSON
        console.log('\n2. Parsing JSON...');
        const modelJson = JSON.parse(fs.readFileSync(modelPath, 'utf8'));
        console.log('   JSON parsing: OK');
        console.log('   Format:', modelJson.format);
        console.log('   Generated by:', modelJson.generatedBy);
        
        // Step 3: Check weights files
        console.log('\n3. Checking weights...');
        const weightsManifest = modelJson.weightsManifest;
        if (weightsManifest && weightsManifest.length > 0) {
            console.log('   Weights manifest entries:', weightsManifest.length);
            weightsManifest.forEach((entry, i) => {
                console.log(`   Entry ${i}:`, entry.paths);
                entry.paths.forEach(weightPath => {
                    const fullWeightPath = path.join(path.dirname(modelPath), weightPath);
                    try {
                        const weightStats = fs.statSync(fullWeightPath);
                        console.log(`     ${weightPath}: ${weightStats.size} bytes - OK`);
                    } catch (error) {
                        console.log(`     ${weightPath}: MISSING or ERROR - ${error.message}`);
                    }
                });
            });
        } else {
            console.log('   No weights manifest found');
        }
        
        // Step 4: Try to create the model URL
        console.log('\n4. Creating model URL...');
        const modelUrl = `file://${modelPath}`;
        console.log('   Model URL:', modelUrl);
        
        // Step 5: Set up a more aggressive timeout
        console.log('\n5. Attempting to load model with aggressive timeout...');
        
        const timeoutPromise = new Promise((_, reject) => {
            setTimeout(() => {
                reject(new Error('Model loading timed out after 3 seconds'));
            }, 3000);
        });
        
        const loadPromise = (async () => {
            console.log('   Starting tf.loadLayersModel...');
            const model = await tf.loadLayersModel(modelUrl);
            console.log('   Model loaded successfully!');
            return model;
        })();
        
        const model = await Promise.race([loadPromise, timeoutPromise]);
        
        console.log('   SUCCESS: Model loaded!');
        console.log('   Model summary:');
        model.summary();
        
    } catch (error) {
        console.log('\nERROR:', error.message);
        if (error.stack) {
            console.log('STACK:', error.stack);
        }
    }
}

debugModelLoading();
