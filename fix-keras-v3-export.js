const fs = require('fs');
const path = require('path');

console.log('Fixing Keras v3 export issues...');

// Read the model.json file
const modelPath = 'public/tfjs_multi_output_model/model.json';
const modelData = JSON.parse(fs.readFileSync(modelPath, 'utf8'));

console.log('Original format - Generated by:', modelData.generatedBy);
console.log('Original format - Keras version:', modelData.modelTopology?.keras_version);

// Fix the layers
const layers = modelData.modelTopology.model_config.config.layers;
let fixedLayers = 0;
let fixedNodes = 0;

layers.forEach(layer => {
  // Fix InputLayer batch_shape -> batchInputShape
  if (layer.class_name === 'InputLayer' && layer.config.batch_shape) {
    layer.config.batchInputShape = layer.config.batch_shape;
    delete layer.config.batch_shape;
    fixedLayers++;
    console.log(`Fixed InputLayer: ${layer.name}`);
  }

  // Fix inbound_nodes format from Keras v3 to TensorFlow.js compatible
  if (layer.inbound_nodes && Array.isArray(layer.inbound_nodes)) {
    layer.inbound_nodes = layer.inbound_nodes.map(node => {
      // If it's the new Keras v3 format with args/kwargs
      if (node && typeof node === 'object' && 'args' in node) {
        // Convert to old format: [layer_name, node_index, tensor_index]
        if (node.args && Array.isArray(node.args) && node.args.length > 0) {
          const firstArg = node.args[0];
          if (firstArg && firstArg.config && firstArg.config.keras_history) {
            const [layerName, nodeIndex, tensorIndex] = firstArg.config.keras_history;
            fixedNodes++;
            return [layerName, nodeIndex, tensorIndex];
          }
        }
        // Fallback: return empty array if we can't parse
        return [];
      }
      // If it's already in the correct format, keep it
      return node;
    });
  }
});

console.log(`Fixed ${fixedLayers} InputLayers and ${fixedNodes} inbound nodes`);

// Update metadata to indicate it's been fixed
modelData.generatedBy = 'keras v3.10.0 (fixed for TensorFlow.js compatibility)';

// Write the fixed model back
fs.writeFileSync(modelPath, JSON.stringify(modelData, null, 2));
console.log('Fixed model saved successfully!');

// Verify the fixes
console.log('\nVerification:');
const verifyData = JSON.parse(fs.readFileSync(modelPath, 'utf8'));
const verifyLayers = verifyData.modelTopology.model_config.config.layers;
const inputLayer = verifyLayers.find(l => l.class_name === 'InputLayer');
console.log('InputLayer now has batchInputShape:', 'batchInputShape' in inputLayer.config);
console.log('InputLayer no longer has batch_shape:', !('batch_shape' in inputLayer.config));

const layerWithNodes = verifyLayers.find(l => l.inbound_nodes && l.inbound_nodes.length > 0);
if (layerWithNodes && layerWithNodes.inbound_nodes[0]) {
  console.log('Inbound nodes now in array format:', Array.isArray(layerWithNodes.inbound_nodes[0]));
  console.log('Sample inbound node:', layerWithNodes.inbound_nodes[0]);
}
